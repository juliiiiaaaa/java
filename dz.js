function outputNumbers() {
let number = 100
 for (let i=0; i <= number; i++) {
  if (i === 5) {
      console.log('пять')
   } else if (i === 13) {
      console.log('тринадцать')
   } else if (i === 22) {
       console.log('двадцать два')
   } else if (i === 35) {
       console.log ('тридцать пять')
   } else if (i === 98) {
       console.log('девяносто восемь')
   } else {
       console.log(i)
   }
}
}
outputNumbers()